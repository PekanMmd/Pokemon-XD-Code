func colour(named name: String) -> Int? {
	return coloursByName[name.simplified]
}

func colour(forType type: XGMoveTypes) -> Int? {
	var colourName = "grey"
	switch type.rawValue {
	case XGMoveTypes.normal.rawValue: colourName = "tan"
	case XGMoveTypes.fighting.rawValue: colourName = "brown"
	case XGMoveTypes.flying.rawValue: colourName = "lavender"
	case XGMoveTypes.poison.rawValue: colourName = "mediumorchid"
	case XGMoveTypes.ground.rawValue: colourName = "peru"
	case XGMoveTypes.rock.rawValue: colourName = "saddlebrown"
	case XGMoveTypes.bug.rawValue: colourName = "darkseagreen"
	case XGMoveTypes.ghost.rawValue: colourName = "darkslateblue"
	case XGMoveTypes.steel.rawValue: colourName = "gray"
	case XGMoveTypes.fairy.rawValue: colourName = "violet"
	case XGMoveTypes.fire.rawValue: colourName = "orangered"
	case XGMoveTypes.water.rawValue: colourName = "dodgerblue"
	case XGMoveTypes.grass.rawValue: colourName = "olivedrab"
	case XGMoveTypes.electric.rawValue: colourName = "khaki"
	case XGMoveTypes.psychic.rawValue: colourName = "pink"
	case XGMoveTypes.ice.rawValue: colourName = "powderblue"
	case XGMoveTypes.dragon.rawValue: colourName = "mediumvioletred"
	case XGMoveTypes.dark.rawValue: colourName = "midnightblue"
	default: break
	}
	return colour(named: colourName)
}
